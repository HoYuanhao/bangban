package com.xunyanhui.bean;

public class Distance {
private String id;
private Double lng;
private Double lat;
public String getId() {
	return id;
}
public void setId(String id) {
	this.id = id;
}
public Double getLng() {
	return lng;
}
public void setLng(Double lng) {
	this.lng = lng;
}
public Double getLat() {
	return lat;
}
public void setLat(Double lat) {
	this.lat = lat;
}
}
